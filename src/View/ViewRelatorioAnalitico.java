    //Criação da tabela com os intens pesquisados da minha Comanda.
    public void loadTable(ArrayList<String[]> listaItens) {

        DefaultTableModel tmItens = new DefaultTableModel();

        tmItens.addColumn("Produto");
        tmItens.addColumn("Quantidade");
        tmItens.addColumn("Valor Total Produto");        

        tabelaRelatorio.setModel(tmItens);

        for (String[] elementosItens : listaItens) {

            tmItens.addRow(elementosItens);
        }

        tabelaRelatorio.getColumnModel().getColumn(0).setPreferredWidth(250); //Produto
        tabelaRelatorio.getColumnModel().getColumn(1).setPreferredWidth(250); //Quantidade
        tabelaRelatorio.getColumnModel().getColumn(2).setPreferredWidth(250); //Valor Total        

    }

