    //Botão de pesquisa de Comanda, no qual traz as informaçoes do meu da minha comanda e dos Itens que tem Cadastrados
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<Comanda> comanda = ComandaController.getComandaList();
        boolean find = false;
        ArrayList<String[]> listaItens = new ArrayList();
        
        for (Comanda c : comanda) {
            //Valida se existe uma Comanda igual ao digitado
            if (c.getIdComanda() == Integer.parseInt(txtIdComanda.getText())) {
                find = true;
                ArrayList<itemComanda> item = c.getItemComanda();
                //passa de parametro a lista de itens da comanda em questão
                listaItens = ComandaController.getListaComanda(item, listaItens);
                this.loadTable(listaItens);
                //Preenche os campos com os dados de Nome do Cliente e a Data da compra da Comanda em Questão
                txtNomeCliente.setText(c.getNomeCliente());
                txtData.setText(c.getData());
                JOptionPane.showMessageDialog(this, "Detalhes da Venda", "Venda", JOptionPane.INFORMATION_MESSAGE);
                
            }
        }
        if (find == false) {
            JOptionPane.showMessageDialog(this, "Não localidazo", "Venda", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

